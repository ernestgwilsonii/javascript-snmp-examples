const { snmpGetInfo } = require('./snmpGetInfo.js');
////////////////////////////////////////////////////////////////////////////////
// SNMP "get" Promise wrapper //
////////////////////////////////
function callSnmpGet(snmpRequest) {
    return new Promise((resolve, reject) => {
        snmpGetInfo(snmpRequest)
            .then((res) => {
                resolve(res);
            }).catch(err => {
                reject(err)
            });
    })
}
module.exports.callSnmpGet = callSnmpGet;
